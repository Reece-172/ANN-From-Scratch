import sklearn.decomposition as skd
import sklearn.preprocessing as skp


def normalize(data):
    return skp.normalize(data)


def pca(data,
        n):  # perform PCA on data to reduce dimensionality (change the n_components / features to see how it affects the results)
    data = normalize(data)
    pca = skd.PCA(n_components=n)
    pca.fit(data)
    return pca.transform(data)


def softmax(Z):
    return np.exp(Z) / np.sum(np.exp(Z), axis=0)


def sigmoid(Z):
    return 1 / (1 + np.exp(-Z))


def forward_prop(X, w1, b1, w2, b2, w3, b3):  # forward propagation
    Z1 = np.dot(w1, X) + b1
    A1 = sigmoid(Z1)
    Z2 = np.dot(w2, A1) + b2
    A2 = sigmoid(Z2)
    Z3 = np.dot(w3, A2) + b3
    A3 = softmax(Z3)
    return A3  # returns the output of the network


def get_prediction(A3):
    return np.argmax(A3, 0)  # returns the index of the highest value in each row from the output of the network


def get_accuracy(predictions, Y):
    accuracy = np.sum(predictions == Y) / Y.size
    return accuracy * 100


